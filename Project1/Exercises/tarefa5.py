def count_gender(data_list):
    """Função para contar os gêneros. Retorne uma lista."""
    male = 0
    female = 0
    for data in data_list:
        if data[-2] == "Male":
            male += 1
        elif data[-2] == "Female":
            female += 1
    return [male, female]


